import { readFileSync } from "node:fs";

import { defineConfig } from "astro/config";
import { unified } from "@astrojs/markdown-remark";
import starlight from "@astrojs/starlight";
import blackTheme from "starlight-theme-black";

import { rehypeBasePath } from "./src/plugins/rehype-base-path.mjs";

// `site` and `base` follow the GitHub Pages project URL. Both are overridable so a fork
// can publish under its own account without editing the config.
const site = process.env.SITE_URL ?? "https://www.rodrigoroque.dev";
const base = process.env.BASE_PATH ?? "/transporte-sp";

// The sidebar is generated with the dataset: Starlight's autogenerated groups take their
// label from the directory on disk, which would show every line as "linha-1-azul".
const linhas = JSON.parse(
  readFileSync(new URL("./src/data/sidebar.json", import.meta.url), "utf8"),
);

// Station pages used to live under the line that served them. They now sit at
// `/estacao/<slug>/`, and every previous address is kept working.
// The generated map is base-agnostic, like every other path the pipeline writes; the
// destination is prefixed here, where the base is known. Astro resolves the source patterns
// against the base on its own, but not the targets.
const redirects = Object.fromEntries(
  Object.entries(
    JSON.parse(readFileSync(new URL("./src/data/redirects.json", import.meta.url), "utf8")),
  ).map(([de, para]) => [de, `${base.replace(/\/$/, "")}${para}/`]),
);

export default defineConfig({
  site,
  base,
  redirects,
  trailingSlash: "always",
  markdown: {
    processor: unified({ rehypePlugins: [rehypeBasePath(base)] }),
  },
  integrations: [
    starlight({
      plugins: [
        blackTheme({
          navLinks: [
            { label: "Mapa da rede", slug: "mapa" },
            { label: "Linhas", slug: "linhas" },
            { label: "Metodologia", slug: "metodologia" },
            { label: "Fontes", slug: "fontes" },
          ],
          // The theme offers "open this page in ChatGPT/Claude" buttons. They send the page
          // to a third party and add nothing to a dataset meant to be read and cited.
          docs: { showMarkdownActions: false },
        }),
      ],
      title: "Transporte SP",
      description:
        "Base de dados aberta e auditável do transporte de massa metropolitano de São Paulo.",
      defaultLocale: "root",
      locales: { root: { label: "Português", lang: "pt-BR" } },
      social: [
        {
          icon: "github",
          label: "GitHub",
          href: "https://github.com/roquerodrigo/transporte-sp",
        },
      ],
      customCss: ["./src/styles/custom.css", "./src/styles/tables.css"],
      // Injected here rather than from a `Head` override: themes routinely override
      // `Head`, and whichever override loses simply does not render.
      head: [
        {
          tag: "script",
          content: readFileSync(
            new URL("./src/scripts/wrap-tables.js", import.meta.url),
            "utf8",
          ),
        },
      ],
      sidebar: [
        { label: "Mapa da rede", link: "/mapa/" },
        { label: "Todas as linhas", link: "/linhas/" },
        ...linhas,
        {
          label: "Sobre os dados",
          items: [
            { label: "Metodologia", link: "/metodologia/" },
            { label: "Fontes", link: "/fontes/" },
            { label: "Divergências", link: "/divergencias/" },
          ],
        },
      ],
    }),
  ],
});
